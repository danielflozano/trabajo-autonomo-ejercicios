package tipos_datos_variables;

/*
 * Escribe un programa que declare variables de los tipos de datos primitivos en Java
 * (int, double, char, boolean) e imprima sus valores.
 */

public class Ejercicio1 {

	public static void main(String[] args) {
		
		int numero = 10;
        double decimal = 5.5;
        char letra = 'A';
        boolean verdadero = true;

        System.out.println("Entero: " + numero);
        System.out.println("Decimal: " + decimal);
        System.out.println("Caracter: " + letra);
        System.out.println("Booleano: " + verdadero);
        
	}

}
